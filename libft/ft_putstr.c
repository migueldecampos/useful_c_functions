#include "libft.h"

static void	ft_char(char c)
{
	write(1, &c, 1);
}

void		ft_putstr(char const *s)
{
	int i;

	i = 0;
	if (s != NULL)
		while (s[i] != '\0')
		{
			ft_char(s[i]);
			i = i + 1;
		}
}
