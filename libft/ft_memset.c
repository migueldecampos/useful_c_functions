#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t	i;
	char	*out;

	i = 0;
	out = (char *)b;
	while (i < len)
	{
		out[i] = c;
		i = i + 1;
	}
	return (b);
}
